package Model;

public class Ficha{    
    private int idFicha;
    private int idIndustria;
    private int idAgente;
    private int idAtividade;
    private int confirmacao;
    private String observacao;
    private String dataFicha;

    public int getIdFicha() {
        return idFicha;
    }

    public void setIdFicha(int idFicha) {
        this.idFicha = idFicha;
    }   

    public int getIdIndustria() {
        return idIndustria;
    }

    public void setIdIndustria(int idIndustria) {
        this.idIndustria = idIndustria;
    }

    public int getIdAgente() {
        return idAgente;
    }

    public void setIdAgente(int idAgente) {
        this.idAgente = idAgente;
    }

    public int getIdAtividade() {
        return idAtividade;
    }

    public void setIdAtividade(int idAtividade) {
        this.idAtividade = idAtividade;
    }

    public int getConfirmacao() {
        return confirmacao;
    }

    public void setConfirmacao(int i) {
        this.confirmacao = i;
    }

    public String getObservacao() {
        return observacao;
    }

    public void setObservacao(String observacao) {
        this.observacao = observacao;
    }   

    public String getDataFicha() {
        return dataFicha;
    }

    public void setDataFicha(String dataFicha) {
        this.dataFicha = dataFicha;
    }

    
    
}


